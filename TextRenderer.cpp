// Шейдеры (как в предыдущей версии)
static const char* vertexShaderSource = R"(
#version 330 core
layout(location = 0) in vec2 aPos;
layout(location = 1) in vec2 aTexCoord;
out vec2 TexCoord;
uniform mat4 uProjection;
void main() {
    gl_Position = uProjection * vec4(aPos, 0.0, 1.0);
    TexCoord = aTexCoord;
}
)";

static const char* fragmentShaderSource = R"(
#version 330 core
in vec2 TexCoord;
out vec4 FragColor;
uniform sampler2D uTexture;
uniform vec3 uColor;
void main() {
    float alpha = texture(uTexture, TexCoord).r;
    FragColor = vec4(uColor, alpha);
}
)";

TextRenderer::TextRenderer() : m_VAO(0), m_VBO(0), m_shaderProgram(0), m_textureAtlas(0), 
                                m_screenWidth(800), m_screenHeight(600) {}

TextRenderer::~TextRenderer() {
    cleanup();
}

int TextRenderer::charToIndex(char c) const {
    int idx = (int)c - 32;
    if (idx < 0 || idx >= 95) return 0;
    return idx;
}

void TextRenderer::buildTextureAtlas() {
    const int CHAR_WIDTH = 8;
    const int CHAR_HEIGHT = 8;
    const int CHARS_PER_ROW = 16;
    const int ATLAS_WIDTH = CHARS_PER_ROW * CHAR_WIDTH;
    const int ATLAS_HEIGHT = ((95 + CHARS_PER_ROW - 1) / CHARS_PER_ROW) * CHAR_HEIGHT;
    
    std::vector<unsigned char> atlasData(ATLAS_WIDTH * ATLAS_HEIGHT, 0);
    
    for (int i = 0; i < 95; i++) {
        int x = (i % CHARS_PER_ROW) * CHAR_WIDTH;
        int y = (i / CHARS_PER_ROW) * CHAR_HEIGHT;
        
        for (int row = 0; row < CHAR_HEIGHT; row++) {
            unsigned char bits = m_fontData[i][row];
            for (int col = 0; col < CHAR_WIDTH; col++) {
                if (bits & (0x80 >> col)) {
                    atlasData[(y + row) * ATLAS_WIDTH + (x + col)] = 255;
                }
            }
        }
    }
    
    glGenTextures(1, &m_textureAtlas);
    glBindTexture(GL_TEXTURE_2D, m_textureAtlas);
    glTexImage2D(GL_TEXTURE_2D, 0, GL_RED, ATLAS_WIDTH, ATLAS_HEIGHT, 0, GL_RED, GL_UNSIGNED_BYTE, atlasData.data());
    glTexParameteri(GL_TEXTURE_2D, GL_TEXTURE_MIN_FILTER, GL_NEAREST);
    glTexParameteri(GL_TEXTURE_2D, GL_TEXTURE_MAG_FILTER, GL_NEAREST);
}

void TextRenderer::setupShader() {
    unsigned int vertexShader = glCreateShader(GL_VERTEX_SHADER);
    glShaderSource(vertexShader, 1, &vertexShaderSource, nullptr);
    glCompileShader(vertexShader);
    
    unsigned int fragmentShader = glCreateShader(GL_FRAGMENT_SHADER);
    glShaderSource(fragmentShader, 1, &fragmentShaderSource, nullptr);
    glCompileShader(fragmentShader);
    
    m_shaderProgram = glCreateProgram();
    glAttachShader(m_shaderProgram, vertexShader);
    glAttachShader(m_shaderProgram, fragmentShader);
    glLinkProgram(m_shaderProgram);
    
    glDeleteShader(vertexShader);
    glDeleteShader(fragmentShader);
}

bool TextRenderer::init(int screenWidth, int screenHeight) {
    m_screenWidth = screenWidth;
    m_screenHeight = screenHeight;
    
    setupShader();
    buildTextureAtlas();
    
    glGenVertexArrays(1, &m_VAO);
    glGenBuffers(1, &m_VBO);
    
    glBindVertexArray(m_VAO);
    glBindBuffer(GL_ARRAY_BUFFER, m_VBO);
    glBufferData(GL_ARRAY_BUFFER, sizeof(float) * 6 * 4, nullptr, GL_DYNAMIC_DRAW);
    
    glVertexAttribPointer(0, 2, GL_FLOAT, GL_FALSE, 4 * sizeof(float), (void*)0);
    glEnableVertexAttribArray(0);
    glVertexAttribPointer(1, 2, GL_FLOAT, GL_FALSE, 4 * sizeof(float), (void*)(2 * sizeof(float)));
    glEnableVertexAttribArray(1);
    
    glBindVertexArray(0);
    
    return true;
}

void TextRenderer::renderText(const std::string& text, float x, float y, float scale, const Color& color) {
    if (m_shaderProgram == 0) return;
    
    glDisable(GL_DEPTH_TEST);
    glEnable(GL_BLEND);
    glBlendFunc(GL_SRC_ALPHA, GL_ONE_MINUS_SRC_ALPHA);
    
    glUseProgram(m_shaderProgram);
    
    mat4 projection = mat4::ortho(0.0f, (float)m_screenWidth, (float)m_screenHeight, 0.0f, -1.0f, 1.0f);
    glUniformMatrix4fv(glGetUniformLocation(m_shaderProgram, "uProjection"), 1, GL_FALSE, projection.ptr());
    glUniform3f(glGetUniformLocation(m_shaderProgram, "uColor"), color.r, color.g, color.b);
    
    glActiveTexture(GL_TEXTURE0);
    glBindTexture(GL_TEXTURE_2D, m_textureAtlas);
    glUniform1i(glGetUniformLocation(m_shaderProgram, "uTexture"), 0);
    
    glBindVertexArray(m_VAO);
    
    const int CHAR_WIDTH = 8;
    const int CHAR_HEIGHT = 8;
    const int CHARS_PER_ROW = 16;
    
    float startX = x;
    for (char c : text) {
        int idx = charToIndex(c);
        float atlasX = (idx % CHARS_PER_ROW) * CHAR_WIDTH;
        float atlasY = (idx / CHARS_PER_ROW) * CHAR_HEIGHT;
        float atlasW = CHAR_WIDTH;
        float atlasH = CHAR_HEIGHT;
        
        float xpos = startX;
        float ypos = y;
        float w = CHAR_WIDTH * scale;
        float h = CHAR_HEIGHT * scale;
        
        float vertices[6][4] = {
            { xpos,     ypos + h,    atlasX / 128.0f, (atlasY + atlasH) / 128.0f },
            { xpos + w, ypos,        (atlasX + atlasW) / 128.0f, atlasY / 128.0f },
            { xpos,     ypos,        atlasX / 128.0f, atlasY / 128.0f },
            
            { xpos,     ypos + h,    atlasX / 128.0f, (atlasY + atlasH) / 128.0f },
            { xpos + w, ypos + h,    (atlasX + atlasW) / 128.0f, (atlasY + atlasH) / 128.0f },
            { xpos + w, ypos,        (atlasX + atlasW) / 128.0f, atlasY / 128.0f }
        };
        
        glBindBuffer(GL_ARRAY_BUFFER, m_VBO);
        glBufferSubData(GL_ARRAY_BUFFER, 0, sizeof(vertices), vertices);
        
        glDrawArrays(GL_TRIANGLES, 0, 6);
        
        startX += CHAR_WIDTH * scale;
    }
    
    glBindVertexArray(0);
    glEnable(GL_DEPTH_TEST);
}

void TextRenderer::cleanup() {
    if (m_textureAtlas) glDeleteTextures(1, &m_textureAtlas);
    if (m_VAO) glDeleteVertexArrays(1, &m_VAO);
    if (m_VBO) glDeleteBuffers(1, &m_VBO);
    if (m_shaderProgram) glDeleteProgram(m_shaderProgram);
    m_textureAtlas = m_VAO = m_VBO = m_shaderProgram = 0;
}